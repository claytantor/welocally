package com.welocally.geodb.services.spatial;

public class SpatialIndexException extends Exception {

	public SpatialIndexException() {
	}

	public SpatialIndexException(String arg0) {
		super(arg0);
	}

	public SpatialIndexException(Throwable arg0) {
		super(arg0);
	}

	public SpatialIndexException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
