package com.welocally.geodb.services.spatial;

public class SpatialSearchException extends Exception {

	public SpatialSearchException() {
	}

	public SpatialSearchException(String arg0) {
		super(arg0);
	}

	public SpatialSearchException(Throwable arg0) {
		super(arg0);
	}

	public SpatialSearchException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
