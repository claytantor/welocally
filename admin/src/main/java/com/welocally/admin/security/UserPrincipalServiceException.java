package com.welocally.admin.security;

public class UserPrincipalServiceException extends Exception {

	
	public UserPrincipalServiceException() {
		super();
	}

	public UserPrincipalServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public UserPrincipalServiceException(String message) {
		super(message);
	}
	

	public UserPrincipalServiceException(Throwable cause) {
		super(cause);
	}


	
	

}
